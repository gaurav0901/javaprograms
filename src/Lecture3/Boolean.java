public class Boolean{
	public static void main(String[] args){
		int x = 4;
		int y = 7;
		boolean t = true;
		boolean f = false;
		System.out.println("x is "+ x);
		System.out.println("y is "+ y);
		System.out.println("x < y is "+ (x<y));
		System.out.println("x > y is "+ (x>y));
		System.out.println("x <= y is "+ (x<=y));
		System.out.println("x >= y is "+ (x>=y));
		System.out.println("x = y is "+ (x==y));
		System.out.println("x != y is "+ (x!=y));
		System.out.println("t is "+ t);
		System.out.println("f is "+ f);
		System.out.println("!t is "+ !t);
		System.out.println("!f is "+ !f);


	}
}